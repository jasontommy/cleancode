package zhangyi.cleancode.alarmparser;

public class XMLParserException extends Exception {
    /**
     *
     */
    private static final long serialVersionUID = -3948909716831076803L;

    public XMLParserException() {
        super();
    }

    public XMLParserException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public XMLParserException(String arg0) {
        super(arg0);
    }

    public XMLParserException(Throwable arg0) {
        super(arg0);
    }

}
